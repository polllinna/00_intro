#include <iostream>
using namespace std;

int main()
{
	int x = 13;
	int y = 73;
	cout << " x = " << x << endl;
	cout << " y = " << y << endl;
	cout << " x + y = " << x + y << endl;
	cout << " x - y = " << x - y << endl;
	cout << " x / y = " << x / y << endl;
	cout << " x % y = " << x % y << endl;
	return 0;
}